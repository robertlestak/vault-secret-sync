// NewClients creates and returns a new vault client with a valid token or error
func (vc *VaultClient) NewClient(ctx context.Context) (*api.Client, error) {
	log.Tracef("vault.NewClient")
	config := &api.Config{
		Address: vc.Address,
	}
	var err error
	vc.Client, err = api.NewClient(config)
	if err != nil {
		return vc.Client, err
	}
	if vc.Namespace != "" {
		vc.Client.SetNamespace(vc.Namespace)
	}
	terr := vc.NewToken(ctx)
	if terr != nil {
		return vc.Client, terr
	}
	vc.Client.AddHeader("x-vault-sync", "true")
	return vc.Client, err
}

// Login creates a vault token with the k8s auth provider
func (vc *VaultClient) Login(ctx context.Context) error {
	l := log.WithFields(log.Fields{
		"address":   vc.Address,
		"role":      vc.Role,
		"path":      vc.Path,
		"method":    vc.AuthMethod,
		"namespace": vc.Namespace,
	})
	l.Trace("vault.Login")
	if vc.Client == nil {
		_, err := vc.NewClient(ctx)
		if err != nil {
			return err
		}
	}
	var kubeTokenExists bool
	ktp := "/var/run/secrets/kubernetes.io/serviceaccount/token"
	if _, err := os.Stat(ktp); !os.IsNotExist(err) {
		l.Tracef("kubeToken exists at path=%s", ktp)
		kubeTokenExists = true
	}
	var kt string
	if kubeTokenExists {
		l.Tracef("reading kubeToken from path=%s", ktp)
		fd, err := os.ReadFile(ktp)
		if err != nil {
			return err
		}
		kt = string(fd)
	}
	if kt != "" {
		options := map[string]interface{}{
			"role": vc.Role,
			"jwt":  kt,
		}
		if vc.TTL != "" {
			options["ttl"] = vc.TTL
		}
		path := fmt.Sprintf("auth/%s/login", vc.AuthMethod)
		l.WithFields(log.Fields{
			"path": path,
			"role": vc.Role,
		}).Trace("vault.Login calling Write")
		secret, err := vc.Client.Logical().WriteWithContext(ctx, path, options)
		if err != nil {
			return err
		}
		vc.Client.SetToken(secret.Auth.ClientToken)
	} else {
		vc.Client.SetToken(os.Getenv("VAULT_TOKEN"))
	}
	return nil
}

func tokenEnvTemplate(t string) string {
	l := log.WithFields(log.Fields{
		"action": "tokenEnvTemplate",
	})
	l.Trace("start")
	if !strings.Contains(t, "{{") {
		return ""
	}
	evs := strings.Split(t, "{{")
	if len(evs) < 2 {
		return ""
	}
	evs = strings.Split(strings.TrimSpace(evs[1]), "}}")
	if len(evs) < 2 {
		return ""
	}
	ev := evs[0]
	return os.Getenv(ev)
}

func (vc *VaultClient) Init(ctx context.Context) error {
	if err := vc.NewToken(ctx); err != nil {
		return err
	}
	if err := vc.Validate(); err != nil {
		return err
	}
	return nil
}

func (vc *VaultClient) NewToken(ctx context.Context) error {
	l := log.WithFields(log.Fields{
		"address": vc.Address,
		"role":    vc.Role,
		"path":    vc.Path,
		"method":  vc.AuthMethod,
	})
	l.Trace("vault.NewToken calling Login")
	if os.Getenv("VAULT_TOKEN") != "" {
		l.Trace("using VAULT_TOKEN")
		if vc.Client == nil {
			_, err := vc.NewClient(ctx)
			if err != nil {
				return err
			}
		}
		vc.Client.SetToken(os.Getenv("VAULT_TOKEN"))
	}
	if err := vc.Login(ctx); err != nil {
		return err
	}
	return nil
}

func insertSliceString(a []string, index int, value string) []string {
	if len(a) == index { // nil or empty slice or after last element
		return append(a, value)
	}
	a = append(a[:index+1], a[index:]...) // index < len(a)
	a[index] = value
	return a
}

// GetKVSecret retrieves a kv secret from vault
func (vc *VaultClient) GetKVSecretOnce(ctx context.Context, s string) (map[string]interface{}, error) {
	l := log.WithFields(log.Fields{
		"address": vc.Address,
		"role":    vc.Role,
		"path":    s,
		"method":  vc.AuthMethod,
	})
	var secrets map[string]interface{}
	if s == "" {
		return secrets, errors.New("secret path required")
	}
	ss := strings.Split(s, "/")
	if len(ss) < 2 {
		return secrets, errors.New("secret path must be in kv/path/to/secret format")
	}
	ss = insertSliceString(ss, 1, "data")
	//log.Debugf("headers_sent=%+v", vc.Client.Headers())
	c := vc.Client.Logical()
	s = strings.Join(ss, "/")
	if c == nil {
		return secrets, errors.New("vault client not initialized")
	}
	secret, err := c.ReadWithContext(ctx, s)
	if err != nil {
		return secrets, err
	}
	if secret == nil || secret.Data == nil {
		return nil, errors.New("secret not found: " + s)
	}
	l.Tracef("secret=%+v", secret)
	if secret.Data["data"] == nil {
		return nil, errors.New("secret data not found: " + s)
	}
	return secret.Data["data"].(map[string]interface{}), nil
}

// GetKVSecret will login and retry secret access on failure
// to gracefully handle token expiration
func (vc *VaultClient) GetSecret(ctx context.Context, s string) ([]byte, error) {
	var sec map[string]interface{}
	var err error
	terr := vc.NewToken(ctx)
	if terr != nil {
		return nil, terr
	}
	sec, err = vc.GetKVSecretOnce(ctx, s)
	if err != nil {
		terr := vc.NewToken(ctx)
		if terr != nil {
			return nil, terr
		}
		sec, err = vc.GetKVSecretOnce(ctx, s)
		if err != nil {
			return nil, err
		}
	}
	b, err := json.Marshal(sec)
	if err != nil {
		return nil, err
	}
	return b, err
}

// WriteSecret will login and retry secret write on failure
// to gracefully handle token expiration and CAS conflicts
func (vc *VaultClient) WriteSecret(ctx context.Context, meta metav1.ObjectMeta, s string, bData []byte) ([]byte, error) {
	l := log.WithFields(log.Fields{
		"address": vc.Address,
		"role":    vc.Role,
		"path":    s,
		"method":  vc.AuthMethod,
	})
	
	var newData map[string]interface{}
	err := json.Unmarshal(bData, &newData)
	if err != nil {
		return nil, err
	}

	terr := vc.NewToken(ctx)
	if terr != nil {
		return nil, terr
	}

	// Retry loop for CAS conflicts with exponential backoff
	// No max retries - CAS conflicts are transient and will eventually succeed
	maxBackoff := 5 * time.Second
	attempt := 0
	for {
		if attempt > 0 {
			backoff := time.Duration(100*(1<<uint(attempt-1))) * time.Millisecond
			if backoff > maxBackoff {
				backoff = maxBackoff
			}
			l.WithFields(log.Fields{
				"attempt": attempt,
				"backoff": backoff,
			}).Debug("retrying write after CAS conflict")
			time.Sleep(backoff)
		}

		// Get current version and data
		currentVersion, currentData, err := vc.getSecretWithVersion(ctx, s)
		if err != nil && !strings.Contains(err.Error(), "secret not found") {
			// Real error reading secret - fail immediately
			return nil, fmt.Errorf("failed to read secret: %w", err)
		}

		// Prepare data to write
		dataToWrite := newData
		if vc.Merge && currentData != nil {
			dataToWrite = make(map[string]interface{})
			for k, v := range currentData {
				dataToWrite[k] = v
			}
			for k, v := range newData {
				dataToWrite[k] = v
			}
		}

		// Attempt write with CAS
		_, err = vc.WriteSecretOnce(ctx, s, dataToWrite, currentVersion)
		if err != nil {
			if strings.Contains(err.Error(), "check-and-set parameter did not match") ||
				strings.Contains(err.Error(), "cas parameter did not match") {
				l.WithFields(log.Fields{
					"attempt":      attempt,
					"expected_cas": currentVersion,
				}).Warn("CAS conflict detected, retrying")
				
				attempt++
				continue
			}
			
			if attempt == 0 {
				terr := vc.NewToken(ctx)
				if terr != nil {
					return nil, terr
				}
				attempt++
				continue
			}
			
			return nil, fmt.Errorf("failed to write secret: %w", err)
		}

		l.WithFields(log.Fields{
			"attempt": attempt,
			"cas":     currentVersion,
		}).Debug("successfully wrote secret")
		return nil, nil
	}
}

// WriteSecret writes a secret to Vault VaultClient at path p with secret value s
func (vc *VaultClient) WriteSecretOnce(ctx context.Context, p string, s map[string]interface{}, cas *int) (map[string]interface{}, error) {
	var secrets map[string]interface{}
	pp := strings.Split(p, "/")
	if len(pp) < 2 {
		return secrets, errors.New("secret path must be in kv/path/to/secret format")
	}
	pp = insertSliceString(pp, 1, "data")
	p = strings.Join(pp, "/")
	if s == nil {
		return secrets, errors.New("secret data required")
	}
	if p == "" {
		return secrets, errors.New("secret path required")
	}

	// Prepare the data payload
	vd := map[string]interface{}{
		"data": s,
	}

	// Add CAS parameter if provided
	if cas != nil {
		vd["options"] = map[string]interface{}{
			"cas": *cas,
		}
	}

	_, err := vc.Client.Logical().WriteWithContext(ctx, p, vd)
	if err != nil {
		return secrets, err
	}
	return secrets, nil
}

// getSecretWithVersion retrieves both the secret data and current version
func (vc *VaultClient) getSecretWithVersion(ctx context.Context, s string) (*int, map[string]interface{}, error) {
	if s == "" {
		return nil, nil, errors.New("secret path required")
	}

	ss := strings.Split(s, "/")
	if len(ss) < 2 {
		return nil, nil, errors.New("secret path must be in kv/path/to/secret format")
	}

	// Get metadata for current version
	metadataPath := make([]string, len(ss))
	copy(metadataPath, ss)
	metadataPath = insertSliceString(metadataPath, 1, "metadata")
	metadataPathStr := strings.Join(metadataPath, "/")

	metadata, err := vc.Client.Logical().ReadWithContext(ctx, metadataPathStr)
	
	var currentVersion *int = nil
	if err == nil && metadata != nil && metadata.Data != nil {
		if cv, ok := metadata.Data["current_version"]; ok {
			switch v := cv.(type) {
			case json.Number:
				if intVal, err := v.Int64(); err == nil {
					intCas := int(intVal)
					currentVersion = &intCas
				}
			case float64:
				intCas := int(v)
				currentVersion = &intCas
			case int:
				currentVersion = &v
			case int64:
				intCas := int(v)
				currentVersion = &intCas
			}
		}
	}

	// Read secret data
	dataPath := make([]string, len(ss))
	copy(dataPath, ss)
	dataPath = insertSliceString(dataPath, 1, "data")
	dataPathStr := strings.Join(dataPath, "/")

	secret, err := vc.Client.Logical().ReadWithContext(ctx, dataPathStr)
	if err != nil {
		return currentVersion, nil, err
	}
	
	if secret == nil || secret.Data == nil {
		return currentVersion, nil, errors.New("secret not found: " + s)
	}

	if secret.Data["data"] == nil {
		return currentVersion, nil, errors.New("secret data not found: " + s)
	}

	secretData := secret.Data["data"].(map[string]interface{})
	return currentVersion, secretData, nil
}

// DeleteSecret deletes a secret from path p
func (vc *VaultClient) DeleteSecret(ctx context.Context, p string) error {
	l := log.WithFields(log.Fields{
		"address": vc.Address,
		"role":    vc.Role,
		"path":    p,
		"method":  vc.AuthMethod,
	})
	if p == "" {
		return errors.New("secret path required")
	}
	pp := strings.Split(p, "/")
	if len(pp) < 2 {
		return errors.New("secret path must be in kv/path/to/secret format")
	}
	if !strings.Contains(p, "/metadata/") {
		pp = insertSliceString(pp, 1, "metadata")
		p = strings.Join(pp, "/")
	}
	terr := vc.NewToken(ctx)
	if terr != nil {
		return terr
	}
	_, err := vc.Client.Logical().DeleteWithContext(ctx, p)
	if err != nil {
		l.WithFields(log.Fields{
			"error": err,
		}).Error("vault.DeleteSecret")
		return err
	}
	l.Debug("secret deleted")
	return nil
}

func (vc *VaultClient) ListSecretsOnce(ctx context.Context, p string) ([]string, error) {
	if vc == nil || vc.Client == nil {
		return nil, errors.New("vault client not initialized")
	}
	if p == "" {
		return nil, errors.New("secret path required")
	}
	pp := strings.Split(p, "/")
	if len(pp) < 2 {
		return nil, errors.New("secret path must be in kv/path/to/secret format")
	}
	pp = insertSliceString(pp, 1, "metadata")
	p = strings.Join(pp, "/")
	l := log.WithFields(log.Fields{
		"address": vc.Address,
		"role":    vc.Role,
		"path":    p,
		"method":  vc.AuthMethod,
	})
	l.Debug("vault.ListSecrets")
	if !strings.HasSuffix(p, "/") {
		p = p + "/"
	}
	secret, err := vc.Client.Logical().ListWithContext(ctx, p)
	if err != nil {
		return nil, err
	}
	if secret == nil {
		return nil, nil
	}
	k := secret.Data["keys"].([]interface{})
	var keys []string
	for _, v := range k {
		keys = append(keys, v.(string))
	}
	return keys, nil
}

func (vc *VaultClient) ListSecrets(ctx context.Context, p string) ([]string, error) {
	var keys []string
	var err error
	terr := vc.NewToken(ctx)
	if terr != nil {
		return keys, terr
	}
	keys, err = vc.ListSecretsOnce(ctx, p)
	if err != nil {
		terr := vc.NewToken(ctx)
		if terr != nil {
			return keys, terr
		}
		keys, err = vc.ListSecretsOnce(ctx, p)
		if err != nil {
			return keys, err
		}
	}
	return keys, err
}

func (c *VaultClient) Close() error {
	c.Client.ClearToken()
	return nil
}

func (c *VaultClient) SetDefaults(defaults any) error {
	dv, err := json.Marshal(defaults)
	if err != nil {
		return err
	}
	dc := &VaultClient{}
	err = json.Unmarshal(dv, dc)
	if err != nil {
		return err
	}
	if c.Address == "" && dc.Address != "" {
		c.Address = dc.Address
	}
	if c.CIDR == "" && dc.CIDR != "" {
		c.CIDR = dc.CIDR
	}
	if c.AuthMethod == "" && dc.AuthMethod != "" {
		c.AuthMethod = dc.AuthMethod
	}
	if c.Namespace == "" && dc.Namespace != "" {
		c.Namespace = dc.Namespace
	}
	if c.Role == "" && dc.Role != "" {
		c.Role = dc.Role
	}
	if c.TTL == "" && dc.TTL != "" {
		c.TTL = dc.TTL
	}
	return nil
}
